num1 = 4
num2 = 6
# Arithmetic Operators
# (+)
num1 + num2
# (-)
num1 - num2
# (*)
num1 * num2
# (/)
num1 / num2
# (%) ==> reminder
num1 % num2
# (**) ==> a^2 ==> ^ power
num1 ** num2

# Relational Operators
num1 == num2
num1 != num2
num1 <= num2
num1 < num2
num1 >= num2
num1 > num2

# Assignmet operator (+= , -=, *=, **=, /=, %=)
# Logical Operator (not, or, and)
# not will revert the value, True ==> False
print(not(num1 > num2))


